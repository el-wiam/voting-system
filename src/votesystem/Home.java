    private void updatebtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updatebtnActionPerformed
        // TODO add your handling code here:
        String username =usern.getText();
        try {
            Connection connection = DriverManager.getConnection("jdbc:MySQL://localhost:3306/vote", "root", "");
            //query to get the id based on the username (since the username is unique)
            int userID = -1;
            String userIDQuery = "SELECT id FROM voters WHERE username=?";
            try (PreparedStatement IDStatement = connection.prepareStatement(userIDQuery)) {
                IDStatement.setString(1, username);
                ResultSet userIDResultSet =IDStatement.executeQuery();
                if (userIDResultSet.next()) {
                    userID = userIDResultSet.getInt("id");
                }
            }

            if (userID != -1) {
                // Update the profile of the user logged in
                String updateQuery = "UPDATE voters SET full_name=?, email=?, username=?, password=? WHERE id=?";
                try (PreparedStatement updateStatement = connection.prepareStatement(updateQuery)) {
                    // Set parameters for the update statement
                    updateStatement.setString(1, name.getText());
                    updateStatement.setString(2, email.getText());
                    updateStatement.setString(3, usern.getText());
                    updateStatement.setString(4, passwd.getText());
                    updateStatement.setInt(5, userID);

                    // Execute the update statement
                    updateStatement.executeUpdate();

                    System.out.println("Your profile is updated successfully!");
                }
            } else {
                System.out.println("Failed to retrieve user ID for username: " + username);
            }

            connection.close();
        } catch (SQLException e) {
            e.printStackTrace();
        }

    }//GEN-LAST:event_updatebtnActionPerformed

