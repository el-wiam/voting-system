    private int getVoterVotes(int voterID) {
    int voterVotes = 0;

    try {
        Connection connection = DriverManager.getConnection("jdbc:MySQL://localhost:3306/vote", "root", "");
        String query = "SELECT votes FROM voters WHERE id = ?";

        try (PreparedStatement preparedStatement = connection.prepareStatement(query)) {
            preparedStatement.setInt(1, voterID);
            ResultSet resultSet = preparedStatement.executeQuery();

            if (resultSet.next()) {
                voterVotes = resultSet.getInt("votes");
            }
        }

        connection.close();
    } catch (SQLException e) {
        e.printStackTrace();
    }

    return voterVotes;
}
