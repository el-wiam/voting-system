    private void votebtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_votebtnActionPerformed
        // TODO add your handling code here:
        try {
            
        int voterID = getCurrentVoterID();
        int voterVotes = getVoterVotes(voterID);

        // Check if the voter has already voted (votes = 1)
        if (voterVotes == 1) {
            JOptionPane.showMessageDialog(new JFrame(), "You have already voted.", "Error",
                        JOptionPane.ERROR_MESSAGE);
            return; 
        }

        String fullName = (String) names.getSelectedItem();
        int candidateID = 0;
        
        // passant au candidat : recevoir l'id du condidat par son full-name
        Connection connection = DriverManager.getConnection("jdbc:MySQL://localhost:3306/vote", "root", "");
        String query = "SELECT id FROM Condidates WHERE full_name = ?";

        try (PreparedStatement preparedStatement = connection.prepareStatement(query)) {
            preparedStatement.setString(1, fullName);
            ResultSet resultSet = preparedStatement.executeQuery();

            if (resultSet.next()) {
                candidateID = resultSet.getInt("id");
            }
        }

        // Update the number of votes of the candidate in the condidates table
        if (candidateID != 0) {
            String updateQuery = "UPDATE Condidates SET nbdevotes = nbdevotes + 1 WHERE ID = ?";
            try (PreparedStatement updateStatement = connection.prepareStatement(updateQuery)) {
                updateStatement.setInt(1, candidateID);
                updateStatement.executeUpdate();

                // Update the voter's voting status to indicate that they have voted
                updateVoterVotes(voterID);
                
                // Send confirmation email
                sendConfirmationEmail(voterID, fullName);
            }
        }

        connection.close();
    } catch (SQLException e) {
        e.printStackTrace();
    }

    }//GEN-LAST:event_votebtnActionPerformed

